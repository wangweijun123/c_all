	a++;
	printf("a = %d.\n", a);
	
	//a = 4;
	++a;
	printf("a = %d.\n", a);
	
	//a = 4;
	a = a + 1;
	printf("a = %d.\n", a);
	
	//a = 4;
	a += 1;
	printf("a = %d.\n", a);
	
	
